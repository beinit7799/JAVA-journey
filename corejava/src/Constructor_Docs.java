public class Constructor_Docs {
	
	/*
	 * --------------------------------Constructor------------------------------------
	 * #Used to create object in java.
	 * #Special type of method which has same name as class name .
	 * #Does not have return type.
	 * #If there is not any user defined constructor in class then java provides 
	 * 	default constructor to create object.
	 * 
	 * #Types:
	 * 
	 * 		1>Default constructor.
	 * 			->Used to create object and set the default values to object.
	 * 			#Syntax:
	 * 					
	 * 					class_name(){
	 * 
	 * 						//set default value to object.
	 * 					}
	 * 
	 * 		2>Parameterized constructor.
	 * 			->Used to create object and set dynamics values to object.
	 * 			#Syntax:
	 * 					
	 * 					class_namearg1, agg2,agrg3...agr-n(){
	 * 
	 * 						//set dynamic value to object.
	 * 					}
	 */
}
