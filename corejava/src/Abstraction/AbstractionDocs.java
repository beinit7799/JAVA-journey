package Abstraction;

public class AbstractionDocs {
	
	/*
	 * ------------------Abstraction-------------------
	 * # Abstraction is process of hiding implementation details in the process.
	 * # to achieve abstraction:
	  	
	 	a>Abstract class:-Class which contains abstract(unimplemented) method and non abstract method.
	  	
		  =>Abstract method:
		  				->method which does not have body or implementation is known as abstract methods.
		  					eg:- 
		  						abstract  void print();
		  						abstract intArea(int l, int b);
		  	
		  =>every abstract class must have at least one child class and child class must override abstract method.
		  =>We cannot create object of abstract class.
		  =>Level if abstraction 0-100%
		  		syntax:-
		  			abstract class class-name{
		  				//abstract method;
		 				//non abstract method;	
		  			}
		  		
	 * 	
	 * b>Interface:
	 * 	# same as class which contains public abstraction method only.
	 * 	# every interface must have at-least one implementation class and impl. class must override abstract class.
	 * 	# we cannot crate object of interface.
	 *  # level of abstraction is 100%.
	 *  
	 *   	=>Syntax:-
	 *   		
	 *   		interface interface_name{
	 *   			//public abstract methods
	 *   			//public static final variables.
	 *   		}				
	 * 										
	 * 	 
	 */

}
