public class IfElseTest {
	/*
	 * ----------------if_else--------------
	 * used to implement  exactly two condition
	 * 
	 * #syntax:
	 * 	  if(condition){
	 * 			//statement
	 * 		}else{
	 * 			//statement
	 * 		}
	 */
	public static void main(String[] args) {
		int n =3534;
		 if (n%2==0) {
			System.out.println(n+" is even number");
		} else {
			System.out.println(n+" is odd number");
		}
	}

}
